namespace DataTypeBasics
{


public class DataType
{
    //value type
    //Numbers
 
    //integers
    byte a =24;//8 bits
    short b=3453;//16 bits, signed integer (+,-)
    int c=6546547;//32 bits
    long d=44654654757;//64 bits
 
    //Floating numbers
    float e=657.8f;//32 bits, 6-8 digits after decimal place precision
    double f=6546.6;//64 bits, 15-17 digits precision
    decimal g=86879.5m;//128 bits, 28-29 digits precision
 
    //characters
    char h='a';
 
    //boolean types
    bool m= false;//takes true or false values
 
    bool? n=null;//if ? is added, the data type is defined for NULL as well
 
    //reference types
    string o="f\asdf";
    object p="";


 
      
}
}