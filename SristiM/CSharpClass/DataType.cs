public class DataType
{
    //value type
    //Numbers

    //integers
    byte a =24;//8 bits
    short b=3453;//16 bits, signed integer (+,-)
    int c=76767678;//32 bits
    long d=49869438694386;//64 bits

    //Floating pint numbers
    float e=878.8;//32 bits, use when 6-8 digits after decimal place precision
    double f=43543.6;//64 bits, 15-17 digits precision
    decimal g=86879.5;//128 bits, 28-29 digits precision

    //characters
    char h='a';

    //boolean types
    bool m= false;

    bool? n=null;//if ? is added, the data type is defined for NULL as well

    //reference types
    string o="f\asdf";//use of @ makes it verbating string and allows special characters
    object p="";//can hold anything like string, number



      
}