
namespace DataTypeBasics{

public class  DataType
{
    //Value  Types
    //Numbers

    //Integers
    byte a =24; //8 bits
    short b = 345; // 16 bits
    int c = 13123134; //32 bits
    long d = 237479; //64 bits

    //Floating point numbers
    float e = 423423.5f; // 32 bits, point pachi 6-9 digits
    double f = 23748.9; // 64 bits, point pachi 15-17 digits precision
    decimal g = 34242.8m; // 128 bits, point pachi 28-29 digits

    //Character types
    char h = 'r';

    //Boolean types
    bool i = false;
    bool? m = null;

    //Reference types
    string n = @"f\sjdhfjs";
    object o = 1312321;


}
}
