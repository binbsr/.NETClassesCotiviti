namespace DataTypeBasics
{
    public class DataType
    {
        //Value Types
        //Numbers
        //Integers
        byte a = 24; //8 bits
        short b = 345; //16 bits
        int c = 324234234; //32 bits
        long d = 1234444333893; //64 bits

        //floating point numbers
        float e = 324224.8f; //32 bits, 6-9 digits
        double f = 32323232.7; //64 bits, 15-17 digits precision
        decimal g = 3232323.4m; // 128 bits, 28-29 digits

        //character types
        char h = 'r';

        //boolean types
        bool m = false;

        // nullable bool value
        bool? ii = null;

        //Reference Types
        string n = @"f/dsds";
        object o = 12312;


    }




}
